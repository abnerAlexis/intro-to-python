planets = ['Mercury', 'Venus', 'Earth', 'Mars', 'Jupiter', 'Saturn', 'Uranus', 'Neptune']

# Printing the entire list with a for loop
for planet in planets:
    print(planet)

# The range() function
# 5 is the start, 11 is the end, and 1 is the step 
# using range(5, 11) is the same as using range(5, 11, 1)
for i in range(5, 11, 1):
    print(i)

# range()
pets = ['Dog', 'Iguana', 'Cat', 'Snake', 'Turtle']

for i in range(0, 3):
    print(pets[i])